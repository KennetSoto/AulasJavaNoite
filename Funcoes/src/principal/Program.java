package principal;

public class Program {

    /*
    public` é uma palavra-chave que indica que o método pode ser acessado de qualquer lugar, ou seja, ele é público.
    main` é o nome do método que é executado quando o programa é iniciado. Ele é o ponto de entrada do programa.
    Em resumo, `public static void main` é um método público e estático que não retorna nenhum valor e é o ponto de entrada do programa.
     */
    public static void main(String[] args) {
        
        mensagem(); //Chama o método mensagem()
        mensagem2(20.54); //Chama o método mensagem2() passando o valor 20.54 como argumento
        System.out.println(mensagem3()); //Chama o método mensagem3() e imprime o valor retornado na tela
        System.out.println(mensagem4(10)); //Chama o método mensagem4() passando o valor 10 como argumento e imprime o valor retornado na tela
        System.out.println(mensagem4(254)); //Chama o método mensagem4() passando o valor 254 como argumento e imprime o valor retornado na tela
        
        int meuNumero=650; //Declara e inicializa a variável meuNumero com o valor 650
        System.out.println(mensagem4(meuNumero)); //Chama o método mensagem4() passando o valor da variável meuNumero como argumento e imprime o valor retornado na tela
        
 
        
    }
    /*
    static` é uma palavra-chave que indica que a função pertence à classe em vez de uma instância da classe. Isso significa que a função pode ser chamada sem criar um objeto da classe. 
    void` é um tipo de retorno que indica que a função não retorna nenhum valor. Ou seja, a função é usada apenas para executar uma ação, como imprimir uma mensagem na tela.
     */
    static void mensagem() {
       System.out.println("Primeiro void"); //Imprime a mensagem "Primeiro void" na tela
    }
   
    static void mensagem2(double n) {
       System.out.println("O numero informado foi: " + n); //Imprime a mensagem "O numero informado foi: " seguida do valor do argumento n na tela
    }
    
    static String mensagem3() {
        return "Mensagem 3"; //Retorna a string "Mensagem 3"
    }
    
    static int mensagem4(int n) {
        return n*2; //Retorna o dobro do valor do argumento n
    }
}