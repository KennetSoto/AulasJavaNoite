package principal;

public class Program {

	public static void main(String[] args) {
		//Estrutura de repetição são utilizadas para as vezes que
		//precisamos que o programa repita varias vezes algum codigo
		
		
		//for servre quando ja sabemos a quantidade de vezes que 
		//precisamos repetir o codigo
		for(int contador = 0; contador < 10; contador ++) {
		//sintaxe do for: 
		//(1° variavel que inicia o contador; 2° condição que deve ser aceita; 3° incremento)
			System.out.println("valor do contador: "+ (contador+1));
	    
		}
	}

}
